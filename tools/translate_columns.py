def main():
    if not SRC or not Path(SRC).exists():
        raise FileNotFoundError(f"入力CSVが見つかりません: {SRC}")
    df = pd.read_csv(SRC, encoding="utf-8-sig")

    # 前提: 先頭2列が「セクション」「項目」
    # 余計な加工はせず、辞書マップのみで ja 列を生成
    sec_cn  = df.iloc[:, 0].astype(str).fillna("")
    item_cn = df.iloc[:, 1].astype(str).fillna("")
    sec_ja  = [FIX_JA_SECTIONS.get(s, s) for s in sec_cn]
    item_ja = [FIX_JA_ITEMS.get(i, i)    for i in item_cn]

    out = df.copy()
    out.insert(0, "項目_ja", item_ja)
    out.insert(0, "セクション_ja", sec_ja)

    DST.parent.mkdir(parents=True, exist_ok=True)
    out.to_csv(DST, index=False, encoding="utf-8-sig")
    print(f"✅ Saved: {DST}")

if __name__ == "__main__":
    main()
